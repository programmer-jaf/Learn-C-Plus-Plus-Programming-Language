#include <iostream>

using namespace std;

int main()
{
  cout << "Learn about variables" << endl;
  
  // What is comments ?
  // Comments are non-executable lines used to explain code for better understanding
  // Single line comments start with // and end with a new line
  cout << "This is a single line comment" << endl;
  
  // Multi-line comments start with /* and end with */
  /*
  This is a multi-line comment.
  It can span multiple lines.
  */
  cout << "This is a multi-line comment" << endl;
  
  // Variables in C++
  // Variables store data values.
  // They are created using the keyword 'auto', 'register', 'const', or a data type.
  
  // Declare a variable of type int
  int myVariable = 5;
  
  return 0;
}